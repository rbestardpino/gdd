	FROM TESTIGOS_DE_HENRY.BI_FACT_COMPRA_AUTOMOVIL
	JOIN TESTIGOS_DE_HENRY.BI_TIEMPO ON TIEMPO_ID = FC_AUTO_TIEMPO
	GROUP BY FC_AUTO_TIEMPO, TIEMPO_ANIO, TIEMPO_MES, FC_AUTO_SUCURSAL

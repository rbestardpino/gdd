	FROM TESTIGOS_DE_HENRY.BI_FACT_COMPRA_AUTOMOVIL
	JOIN TESTIGOS_DE_HENRY.BI_TIEMPO ON FC_AUTO_TIEMPO = TIEMPO_ID
	GROUP BY FC_AUTO_MODELO
